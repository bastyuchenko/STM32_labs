void BME280_Init(void) {
    uint8_t config[2];
    config[0] = 0xF4;  // ctrl_meas register
    config[1] = 0x27;  // Set oversampling to 0x27
    HAL_I2C_Mem_Write(&hi2c1, BME280_ADDR, config[0], I2C_MEMADD_SIZE_8BIT, &config[1], 1, 100);
}

void BME280_ReadCalibrationData(uint16_t* dig_T1, int16_t* dig_T2, int16_t* dig_T3) {
    uint8_t calib[6];
    HAL_I2C_Mem_Read(&hi2c1, BME280_ADDR, 0x88, I2C_MEMADD_SIZE_8BIT, calib, 6, 100);
    *dig_T1 = (uint16_t)((calib[1] << 8) | calib[0]);
    *dig_T2 = (int16_t)((calib[3] << 8) | calib[2]);
    *dig_T3 = (int16_t)((calib[5] << 8) | calib[4]);
}

int32_t BME280_ReadRawTemperature(void) {
    uint8_t data[3];
    HAL_I2C_Mem_Read(&hi2c1, BME280_ADDR, 0xFA, I2C_MEMADD_SIZE_8BIT, data, 3, 100);
    return (int32_t)(((data[0] << 16) | (data[1] << 8) | data[2]) >> 4);
}

double BME280_CompensateTemperature(int32_t adc_T, uint16_t dig_T1, int16_t dig_T2, int16_t dig_T3) {
    double var1, var2, T;
    var1 = (((double)adc_T) / 16384.0 - ((double)dig_T1) / 1024.0) * ((double)dig_T2);
    var2 = ((((double)adc_T) / 131072.0 - ((double)dig_T1) / 8192.0) * (((double)adc_T) / 131072.0 - ((double)dig_T1) / 8192.0)) * ((double)dig_T3);
    T = (var1 + var2) / 5120.0;
    return T;
}



